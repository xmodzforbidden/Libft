#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;
	size_t	a;

	i = 0;
	if (!s)
		return ;
	a = ft_strlen(s);
	while (i < a)
	{
		(*f)(i, s + i);
		i++;
	}
}
